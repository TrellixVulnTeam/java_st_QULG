package by.rymko.designpatterns.structural.s_01_adapter;

public class JavaApplicationDatabaseAdapter extends JavaApplicationDatabase implements Database {
    @Override
    public void insert() {
        saveObject();
    }

    @Override
    public void update() {
        updateObject();
    }

    @Override
    public void select() {
        loadObject();
    }

    @Override
    public void remove() {
        deleteObject();
    }
}
